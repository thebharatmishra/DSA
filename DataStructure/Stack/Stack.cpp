//Stack implementation in C++

#include <stdlib.h>
#include <iostream>

using namespace std;

#define MAX 10
int size = 0;


// Creating a stack
struct stack{
    int items[MAX];
    int top;
};

typedef struct stack st;

void createEmptyStack(st *s){
    s->top = -1;
}

//Check if the stack is full
int isFull(st *s){
    if(s->top == MAX-1)
    return 1;
    else
    return 0;
}